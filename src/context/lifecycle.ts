export interface InitResult {
  created: string[];
  skipped: string[];
  tokens:  Record<string, number>;
}

export async function initContextDocs(cwd: string, force = false): Promise<InitResult> {
  const dir = CONTEXT_DIR(cwd);
  ensureDir(dir);

  const created: string[] = [];
  const skipped: string[] = [];
  const tokens:  Record<string, number> = {};

  const docs: Array<{ name: string; generate: () => Promise<string> | string }> = [
    { name: 'project_summary.md', generate: () => generateProjectSummary(cwd) },
    { name: 'architecture.md',    generate: () => generateArchitecture(cwd) },
    { name: 'session_summary.md', generate: () => generateSessionSummary() },
    { name: 'progress.md',        generate: () => generateProgress() },
    { name: 'handoff.md',         generate: () => generateHandoff(cwd) },
    { name: 'current_task.md',    generate: () => generateCurrentTask() },
  ];

  for (const doc of docs) {
    const filePath = contextDocPath(cwd, doc.name);
    if (!force && fs.existsSync(filePath)) {
      skipped.push(doc.name);
      continue;
    }
    const content = await doc.generate();
    writeAtomic(filePath, content + '\n');
    tokens[doc.name] = estimateTokens(content);
    created.push(doc.name);
  }

  return { created, skipped, tokens };
}

export async function refreshContextDocs(cwd: string): Promise<InitResult> {
  // Only regenerate the auto-generated ones (not session/progress/current_task — user-maintained)
  const dir = CONTEXT_DIR(cwd);
  ensureDir(dir);

  const created: string[] = [];
  const skipped: string[] = [];
  const tokens:  Record<string, number> = {};

  const autoDocs: Array<{ name: string; generate: () => Promise<string> | string }> = [
    { name: 'project_summary.md', generate: () => generateProjectSummary(cwd) },
    { name: 'architecture.md',    generate: () => generateArchitecture(cwd) },
    { name: 'handoff.md',         generate: () => generateHandoff(cwd) },
  ];

  for (const doc of autoDocs) {
    const filePath = contextDocPath(cwd, doc.name);
    const content  = await doc.generate();
    writeAtomic(filePath, content + '\n');
    tokens[doc.name] = estimateTokens(content);
    created.push(doc.name);
  }

  return { created, skipped, tokens };
}

export function listContextDocs(cwd: string): Array<{ name: string; tokens: number; exists: boolean }> {
  const names = [
    'project_summary.md',
    'architecture.md',
    'session_summary.md',
    'progress.md',
    'handoff.md',
    'current_task.md',
  ];
  return names.map(name => {
    const filePath = contextDocPath(cwd, name);
    const content  = readOrNull(filePath);
    return {
      name,
      exists: content !== null,
      tokens: content ? estimateTokens(content) : 0,
    };
  });
}
