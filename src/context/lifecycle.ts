import fs from 'fs';
import path from 'path';
import glob from 'fast-glob';
import { writeAtomic, readOrNull, ensureDir } from '../utils/fs.js';
import { estimateTokens } from './tokenizer.js';

export interface LifecycleDocs {
  projectSummary:  string;
  architecture:    string;
  sessionSummary:  string;
  progress:        string;
  handoff:         string;
  currentTask:     string;
}

const CONTEXT_DIR = (cwd: string) => path.join(cwd, '.tokenimizer', 'context');

export function contextDocPath(cwd: string, name: string): string {
  return path.join(CONTEXT_DIR(cwd), name);
}

// ---------------------------------------------------------------------------
// project_summary.md — ~500 token target
// ---------------------------------------------------------------------------

async function generateProjectSummary(cwd: string): Promise<string> {
  const pkgPath = path.join(cwd, 'package.json');
  const readmePath = [
    path.join(cwd, 'README.md'),
    path.join(cwd, 'readme.md'),
    path.join(cwd, 'README'),
  ].find(p => fs.existsSync(p));

  let name    = path.basename(cwd);
  let version = '';
  let desc    = '';
  let scripts = '';

  if (fs.existsSync(pkgPath)) {
    try {
      const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
      name    = pkg.name ?? name;
      version = pkg.version ? `v${pkg.version}` : '';
      desc    = pkg.description ?? '';
      const s = pkg.scripts ?? {};
      scripts = Object.entries(s).slice(0, 6)
        .map(([k, v]) => `  ${k}: ${v}`)
        .join('\n');
    } catch { /* ignore */ }
  }

  let readmeExcerpt = '';
  if (readmePath) {
    const lines = fs.readFileSync(readmePath, 'utf8').split('\n').slice(0, 30);
    readmeExcerpt = lines.join('\n');
  }

  // Folder structure (top 2 levels, ignoring noise)
  const IGNORE = ['node_modules', '.git', 'dist', '.tokenimizer', 'coverage', '.next', 'build'];
  const topItems = fs.readdirSync(cwd)
    .filter(f => !IGNORE.includes(f) && !f.startsWith('.'))
    .slice(0, 20);

  const structure = topItems.map(item => {
    const full = path.join(cwd, item);
    const stat = fs.statSync(full);
    if (stat.isDirectory()) {
      const children = fs.readdirSync(full)
        .filter(f => !IGNORE.includes(f) && !f.startsWith('.'))
        .slice(0, 8)
        .join(', ');
      return `  ${item}/  (${children})`;
    }
    return `  ${item}`;
  }).join('\n');

  const lines = [
    `# Project Summary — ${name} ${version}`,
    '',
    desc ? `**Purpose:** ${desc}` : '',
    '',
    readmeExcerpt ? `## Overview\n${readmeExcerpt}` : '',
    '',
    `## Structure\n${structure}`,
    '',
    scripts ? `## Key Scripts\n${scripts}` : '',
    '',
    `_Generated by tokenimizer on ${new Date().toISOString().split('T')[0]}_`,
  ].filter(l => l !== undefined).join('\n').trim();

  return lines;
}

// ---------------------------------------------------------------------------
// architecture.md — ~1000 token target
// ---------------------------------------------------------------------------

async function generateArchitecture(cwd: string): Promise<string> {
  const IGNORE_DIRS = ['node_modules', '.git', 'dist', '.tokenimizer', 'coverage', '.next', 'build'];

  // Find source directories
  const sourceFiles = await glob(['src/**/*.{ts,js,tsx,jsx}', '*.{ts,js}'], {
    cwd,
    ignore: IGNORE_DIRS.map(d => `**/${d}/**`),
    onlyFiles: true,
    deep: 4,
  });

  // Group by directory
  const byDir: Record<string, string[]> = {};
  for (const f of sourceFiles.slice(0, 80)) {
    const dir = path.dirname(f);
    byDir[dir] = byDir[dir] ?? [];
    byDir[dir].push(path.basename(f));
  }

  const dirLines = Object.entries(byDir)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([dir, files]) => `  ${dir}/\n    ${files.join(', ')}`)
    .join('\n');

  // Config files present
  const configFiles = [
    'tsconfig.json', 'package.json', '.eslintrc.json', '.eslintrc.js',
    'vite.config.ts', 'next.config.js', 'next.config.ts',
    'tailwind.config.js', 'tailwind.config.ts', 'Dockerfile',
    '.env.example', 'docker-compose.yml',
  ].filter(f => fs.existsSync(path.join(cwd, f)));

  const lines = [
    `# Architecture — ${path.basename(cwd)}`,
    '',
    '## Source Layout',
    dirLines || '  (no source files detected)',
    '',
    configFiles.length ? `## Config Files Present\n  ${configFiles.join(', ')}` : '',
    '',
    `_Generated by tokenimizer on ${new Date().toISOString().split('T')[0]}_`,
  ].filter(l => l !== undefined).join('\n').trim();

  return lines;
}

