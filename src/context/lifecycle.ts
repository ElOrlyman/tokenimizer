// ---------------------------------------------------------------------------

function generateSessionSummary(): string {
  return [
    `# Session Summary — ${new Date().toISOString().split('T')[0]}`,
    '',
    '**What was worked on this session:**',
    '- [fill in]',
    '',
    '**Decisions made:**',
    '- [fill in]',
    '',
    '**Current state:**',
    '- [what works]',
    '- [what is broken or incomplete]',
    '',
    '**Blockers:**',
    '- [none | describe]',
    '',
    '_Update this file during your session. Run `tokenimizer checkpoint` to snapshot._',
  ].join('\n');
}

function generateProgress(): string {
  return [
    `# Progress — ${new Date().toISOString().split('T')[0]}`,
    '',
    '## In Progress',
    '- [ ] [current task]',
    '',
    '## Done',
    '- [x] [completed task]',
    '',
    '## Backlog',
    '- [ ] [future task]',
    '',
    '_Keep this file updated as tasks complete._',
  ].join('\n');
}

function generateHandoff(cwd: string): string {
  const projectSummaryExists = fs.existsSync(contextDocPath(cwd, 'project_summary.md'));
  const architectureExists   = fs.existsSync(contextDocPath(cwd, 'architecture.md'));
  const progressExists       = fs.existsSync(contextDocPath(cwd, 'progress.md'));

  return [
    `# Handoff — ${new Date().toISOString().split('T')[0]}`,
    '',
    '> Paste this file as the first message of a new AI session to restore context.',
    '',
    '## Project',
    projectSummaryExists
      ? '_See .tokenimizer/context/project_summary.md_'
      : '**Name:** [project name]  \n**Purpose:** [one sentence]  \n**Stack:** [key tech]',
    '',
    '## What Was Done',
    '- [summarize the last session]',
    '',
    '## Current State',
    '- [what works now]',
    '- [what is incomplete]',
    '',
    '## Next Step',
    '[The single most important next action]',
    '',
    progressExists ? '_See .tokenimizer/context/progress.md for full task list._' : '',
    architectureExists ? '_See .tokenimizer/context/architecture.md for project structure._' : '',
    '',
    '_Generated by tokenimizer. Edit before pasting to fill in the blanks._',
  ].filter(l => l !== undefined).join('\n').trim();
}

function generateCurrentTask(): string {
  return [
    `# Current Task — ${new Date().toISOString().split('T')[0]}`,
    '',
    '**Task:** [what you are working on right now]',
    '',
    '**Goal:** [what done looks like]',
    '',
    '**Files involved:**',
    '- [file path]',
    '',
    '**Approach:**',
    '[how you plan to do it]',
    '',
    '_Update this file at the start of each task. Keeps context tight for the AI._',
  ].join('\n');
}

